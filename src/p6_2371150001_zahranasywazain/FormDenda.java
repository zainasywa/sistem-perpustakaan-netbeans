    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data denda yang akan dibayar dari tabel terlebih dahulu!");
        return;
    }

    // Validasi: pastikan jumlah denda valid
    try {
        int denda = Integer.parseInt(jTextField3.getText());
        if (denda <= 0) {
            JOptionPane.showMessageDialog(this, "Tidak ada denda yang perlu dibayar!");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format jumlah denda tidak valid!");
        return;
    }

    // Konfirmasi pembayaran
    int confirm = JOptionPane.showConfirmDialog(
        this, 
        "Apakah Anda yakin ingin membayar denda sebesar Rp " + jTextField3.getText() + "?",
        "Konfirmasi Pembayaran",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    // Proses pembayaran denda
    try (Connection conn = koneksi.getkoneksi()) {
        // Update denda menjadi 0
        String sql = "UPDATE peminjaman SET denda = 0 WHERE idPinjam = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, jTextField1.getText());
        
        int rowsAffected = ps.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Pembayaran denda berhasil!");
            // Refresh data
            loadData();
            resetForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal melakukan pembayaran. Data tidak ditemukan.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan database: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

