    private void loadData() {
    model.setRowCount(0); 
    
    try (Connection conn = koneksi.getkoneksi()) {
        String sql = "SELECT DISTINCT p.idPinjam, p.idBuku, b.judul, p.denda " +  
                     "FROM peminjaman p JOIN buku b ON p.idBuku = b.idBuku " +
                     "WHERE p.denda > 0 AND p.username=?";
        
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, loggedUser);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("idPinjam"),
                rs.getString("idBuku"),
                rs.getString("judul"),
                rs.getInt("denda")
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error load data: " + e.getMessage());
    }
}
        
        private void bayarDenda() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data denda yang akan dibayar!");
            return;
        }

        try (Connection conn = koneksi.getkoneksi()) {
            String sql = "UPDATE peminjaman SET denda=0 WHERE idPinjam=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Pembayaran denda berhasil!");
            loadData();
            resetForm();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + ex.getMessage());
        }
    }
        
    private void loadData(String keyword) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    String query = "SELECT p.idPinjam, p.idBuku, b.judul, p.denda " +
                   "FROM peminjaman p JOIN buku b ON p.idBuku = b.idBuku " +
                   "WHERE p.denda > 0 AND p.username = ? " +
                   "AND (p.idPinjam LIKE ? OR p.idBuku LIKE ? OR b.judul LIKE ?)";

    try (Connection conn = koneksi.getkoneksi();
         PreparedStatement ps = conn.prepareStatement(query)) {

        String param = "%" + keyword + "%";
        ps.setString(1, loggedUser); // Filter berdasarkan user yang login
        ps.setString(2, param); // idPinjam
        ps.setString(3, param); // idBuku
        ps.setString(4, param); // judul buku

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("idPinjam"),
                rs.getString("idBuku"),
                rs.getString("judul"),
                rs.getInt("denda")
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saat mencari: " + e.getMessage());
        e.printStackTrace();
    }
}
        
